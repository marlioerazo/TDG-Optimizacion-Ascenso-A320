def evaluar_fraccion(frac):
    """
    Entrena el modelo usando una fracción de los datos,
    y evalúa su error contra la totalidad de los datos (la función física real).
    """
    # Escalar Y basado en estadísticas globales
    y_min = y_min_global
    y_range = y_range_original
    pesos_valor_alto = 1.0
    pesos_valor_bajo = 1.0

    # Discretización (usando tu misma lógica)
    X_train_dummy = None
    prim = [f'x{i}' for i in range(1, 6)]
    ult = [f'x{i}' for i in range(6, 11)]

    disc_p = KBinsDiscretizer(n_bins=17, encode='onehot-dense', strategy='uniform')
    disc_u = KBinsDiscretizer(n_bins=15, encode='onehot-dense', strategy='uniform')
    disc_p.fit(np.vstack([x_mins[:5], x_maxs[:5]]))
    disc_u.fit(np.vstack([x_mins[5:], x_maxs[5:]]))

    num_binarias = 5*17 + 5*15
    nombres_binarias_sk = [f'x{i}' for i in range(num_binarias)]
    poly = PolynomialFeatures(degree=2, include_bias=True)
    poly.fit(np.zeros((1, num_binarias)))

    d = poly.n_output_features_
    A = np.zeros((d, d))
    b_vec = np.zeros(d)
    lambda_reg = 1.0

    # Entrenamiento con CVXPY por lotes
    semilla_aleatoria = 42
    for chunk in pd.read_csv(ruta_csv, chunksize=50000):
        chunk = chunk.sample(frac=0.8, random_state=semilla_guardada)
        if frac < 1.0:
            chunk = chunk.sample(frac=frac, random_state=semilla_aleatoria)
        if len(chunk.columns) == 11:
            chunk.columns = nuevos_nombres
        chunk = chunk.dropna()
        if len(chunk) == 0:
            continue

        y_train = chunk['fun'].values
        y_train_scaled = (y_train - y_min) / y_range
        pesos = np.where(y_train <= percentil_10, pesos_valor_alto, pesos_valor_bajo)

        X_chunk = chunk[prim + ult]
        Xb_primeras = disc_p.transform(X_chunk[prim])
        Xb_ultimas = disc_u.transform(X_chunk[ult])
        X_binarias = np.hstack([Xb_primeras, Xb_ultimas])
        X_poly = poly.transform(X_binarias)

        X_poly_w = X_poly * pesos[:, None]
        A += X_poly.T @ X_poly_w
        b_vec += X_poly.T @ (pesos * y_train_scaled)

        del X_poly_w, X_poly, Xb_primeras, Xb_ultimas, X_binarias, X_chunk
        gc.collect()

    A = (A + A.T) / 2.0
    coefs = cp.Variable(d)
    objective = cp.Minimize(cp.quad_form(coefs, cp.psd_wrap(A)) - 2 * b_vec @ coefs + lambda_reg * cp.sum_squares(coefs))
    prob = cp.Problem(objective)
    prob.solve(solver=cp.OSQP, verbose=False)
    c_val = coefs.value

    # Evaluación cruzada contra el dataset del 100% por lotes
    sse = 0.0
    sst = 0.0
    n_total = 0

    for chunk in pd.read_csv(ruta_csv, chunksize=50000):
        chunk = chunk.sample(frac=0.8, random_state=semilla_guardada)
        if len(chunk.columns) == 11:
            chunk.columns = nuevos_nombres
        chunk = chunk.dropna()

        y_eval = chunk['fun'].values
        X_eval = chunk[prim + ult]

        Xb_p = disc_p.transform(X_eval[prim])
        Xb_u = disc_u.transform(X_eval[ult])
        X_bin_eval = np.hstack([Xb_p, Xb_u])
        X_poly_eval = poly.transform(X_bin_eval)

        y_pred_scaled = X_poly_eval @ c_val
        y_pred = (y_pred_scaled * y_range) + y_min

        sse += np.sum((y_eval - y_pred) ** 2)
        sst += np.sum((y_eval - y_mean_global) ** 2)
        n_total += len(y_eval)

        del X_poly_eval, X_bin_eval, Xb_p, Xb_u
        gc.collect()

    rmse = np.sqrt(sse / n_total)
    r2 = 1 - (sse / sst) if sst!= 0 else 0.0

    return rmse, r2

