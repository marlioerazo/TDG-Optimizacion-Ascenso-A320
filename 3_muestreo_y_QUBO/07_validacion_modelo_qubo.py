import pickle
import numpy as np
import pandas as pd
from sklearn.preprocessing import PolynomialFeatures
from sklearn.metrics import mean_squared_error, r2_score
from scipy.stats import spearmanr
import gc

# 1. Rutas a los archivos
ruta_csv = "muestreo_de_puntos_SOBOL.csv"
ruta_modelo = "modelo_10D_entrenado_.pkl"

nuevos_nombres = [f'x{i}' for i in range(1, 11)] + ['fun']

print("\nCargando la semilla del entrenamiento...")
try:
    with open("semilla.txt", "r") as f:
        semilla_guardada = int(f.read().strip())
    print(f"Semilla recuperada: {semilla_guardada}")
except FileNotFoundError:
    raise ValueError("No se encontró 'semilla.txt'. Asegúrate de correr el Código '06_ajuste_regresion_ridge.py' primero en esta misma carpeta.")

# ==============================================================================
# 2. CARGAR EL MODELO YA ENTRENADO
# ==============================================================================
print("\nCargando el modelo.pkl ya entrenado...")
with open(ruta_modelo, 'rb') as f:
    modelo_info = pickle.load(f)

disc_p = modelo_info['discretizer_primeras']
disc_u = modelo_info['discretizer_ultimas']
y_min_original = modelo_info['y_min']
y_range_original = modelo_info['y_range']
intercept = modelo_info['intercept']
coefs = modelo_info['coefs']

coeficientes_completos = np.concatenate(([intercept], coefs))

# ==============================================================================
# 3. PREDECIR SOBRE "EL RESTO" (VALIDACIÓN)
# ==============================================================================
print("\nReconstruyendo el espacio de validación y prediciendo...")

prim = [f'x{i}' for i in range(1, 6)]
ult = [f'x{i}' for i in range(6, 11)]

poly = PolynomialFeatures(degree=2, include_bias=True)
first_fit = True

# Usaremos listas para almacenar los resultados y concatenarlos al final (más seguro para memoria)
y_val_real_list = []
y_pred_real_list = []

total_val_rows = 0

# ATENCIÓN: El chunksize DEBE ser 50000 para replicar el comportamiento del Código '06_ajuste_regresion_ridge.py'
for chunk in pd.read_csv(ruta_csv, chunksize=50000, low_memory=False):
    if len(chunk.columns) == 11:
        chunk.columns = nuevos_nombres
    chunk = chunk.dropna()

    # -------------------------------------------------------------------
    # ESTA ES PARA QUE LOS DATOS TENGAN EL RIGOR SOBRE '06_ajuste_regresion_ridge.py'
    # -------------------------------------------------------------------
    train_chunk = chunk.sample(frac=0.8, random_state=semilla_guardada)
    val_chunk = chunk.drop(train_chunk.index) # <- Este es tu verdadero 20%
    # -------------------------------------------------------------------

    if len(val_chunk) > 0:
        y_chunk_real = val_chunk['fun'].values
        X_val = val_chunk.drop('fun', axis=1)

        # Discretizar
        X_bin_batch = np.hstack([disc_p.transform(X_val[prim]), disc_u.transform(X_val[ult])])

        # Polinomio
        if first_fit:
            X_poly_batch = poly.fit_transform(X_bin_batch)
            first_fit = False
        else:
            X_poly_batch = poly.transform(X_bin_batch)

        # Predecir
        y_pred_scaled_batch = X_poly_batch @ coeficientes_completos
        y_pred_batch = (y_pred_scaled_batch * y_range_original) + y_min_original

        # Guardar resultados
        y_val_real_list.append(y_chunk_real)
        y_pred_real_list.append(y_pred_batch)
        total_val_rows += len(y_chunk_real)

    # Limpiar RAM
    del chunk, train_chunk, val_chunk, X_bin_batch, X_poly_batch
    gc.collect()

print(f"-> Predicciones completadas. Puntos validados: {total_val_rows}")

# Unir todas las listas en arrays de numpy
y_val_real = np.concatenate(y_val_real_list)
y_pred_real = np.concatenate(y_pred_real_list)
del y_val_real_list, y_pred_real_list
gc.collect()

# ==============================================================================
# 4. CÁLCULO DE MÉTRICAS FINALES Y SPEARMAN
# ==============================================================================
print("\nCalculando métricas de divergencia y correlación de Spearman...")

rmse_val = np.sqrt(mean_squared_error(y_val_real, y_pred_real))
r2_val = r2_score(y_val_real, y_pred_real)

# Submuestreo para Spearman si hay demasiados datos
if len(y_val_real) > 5_000_000:
    print("Muestra grande detectada: Submuestreando para cálculo de Spearman...")
    idx = np.random.RandomState(42).choice(len(y_val_real), 5_000_000, replace=False)
    rho_s, p_value = spearmanr(y_val_real[idx], y_pred_real[idx])
else:
    rho_s, p_value = spearmanr(y_val_real, y_pred_real)

print("\n======================================================================")
print(" RESULTADOS FINALES SOBRE EL CONJUNTO DE VALIDACIÓN ('EL RESTO')")
print("======================================================================")
print(f"RMSE (Error) sobre datos nuevos : {rmse_val:.4f} kg")
print(f"R^2 (Precisión)                 : {r2_val:.4f}")
print(f"Rho_S (Correlación de Spearman) : {rho_s:.4f}")
print(f"P-Value de Spearman             : {p_value:.2e}")
print("======================================================================")
print(f"\n>>> EL DATO A ASIGNAR:")
print(f"\\rho_{{\\text{{S}}}} = {rho_s:.4f}")